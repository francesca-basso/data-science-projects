room = "bed"
area = 14.0

# if statement for room
if room == "kit" :
    print("Looking around in the kitchen.")
elif room == "bed" :
    print("Looking around in the bedroom.")
else :
    print("Looking around elsewhere.")

# if statement for area
if area > 15 :
    print("Big place!")
elif area > 10 :
    print("Medium size, nice!")
else :
    print("Pretty small.")